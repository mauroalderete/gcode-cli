package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "gcode-cli",
	Short: "Gcode command-line tool to oparate with gcode files",
	Long: `gcode-cli is a command-line tool that help you to apply
masive operations to your gcode files like skew corrections, translations or chekcsum integrity.`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

